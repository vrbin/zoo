package org.example;

public class Employee {
    String firstName;
    String lastName;
    String email;
    String phoneNo;
    double hourRate;


    public Employee(String firstName, String lastName, String email, String phoneNo, double hourRate) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.phoneNo = phoneNo;
        this.hourRate = hourRate;
    }
}
